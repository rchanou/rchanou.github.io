USE ClubspeedV8;
SET XACT_ABORT ON;
SET TRANSACTION ISOLATION LEVEL SERIALIZABLE;
BEGIN TRANSACTION;

IF EXISTS (
    SELECT *
    FROM INFORMATION_SCHEMA.TABLES t
    WHERE t.TABLE_NAME = 'CheckDetails'
)
BEGIN
    IF NOT EXISTS (
        SELECT *
        FROM INFORMATION_SCHEMA.COLUMNS c
        WHERE
                c.TABLE_NAME  = 'CheckDetails'
            AND c.COLUMN_NAME = 'CreatedOn'
    )
    BEGIN
        ALTER TABLE dbo.CheckDetails
        ADD CreatedOn NVARCHAR(MAX)
    END;
END;

COMMIT;
